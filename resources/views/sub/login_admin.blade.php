        $("#show_hide_password button").on('click', function(event) {
            event.preventDefault();
            if($('#show_hide_password input').attr("type") == "text"){
                $('#show_hide_password input').attr('type', 'password');
                $('#i_password').addClass( "fa-eye-slash" ).removeClass( "fa-eye" );
            }else if($('#show_hide_password input').attr("type") == "password"){
                $('#show_hide_password input').attr('type', 'text');
                $('#i_password').removeClass( "fa-eye-slash" ).addClass( "fa-eye" );
            }
        });
    });
    $(function() {
        $('#formExecute').validate({
            rules: {
                username: { required: true, },password: { required: true, },
            },
            messages: {
                username: { required: "Please fill in", },password: { required: "Please fill in", },
            },
            errorElement : 'div',
            errorPlacement: function(error, element) {
                var placement = $(element).data('error');
                if (placement) {
                    $(placement).append(error)
                } else {
                    error.insertAfter(element);
                }
            },
            submitHandler: function(form) {
                swalWithBootstrapButtons.fire({
                    text: 'Are you sure?',
                    icon: 'warning',
                    showCancelButton: true,
                    confirmButtonColor: '#3085d6',
                    cancelButtonColor: '#d33',
                    confirmButtonText: 'Yes',
                    cancelButtonText: 'Cancel',
                    reverseButtons: true,
                    showLoaderOnConfirm: true,
                    preConfirm: function () {
                        return new Promise(function (resolve) {
                            var values = $('#formExecute').serialize();
                            $.ajax({
                                type: "POST",data:values, url: "{{ url('/api/jsonLoginAdmin') }}",
                                dataType: 'json', headers: {'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')},
                                success: function(response) {
                                    if (response.status === "OK"){
                                        window.location.replace(response.data.url);
                                    } else {
                                        swalWithBootstrapButtons.fire({icon: 'error', title: 'Oops...', text: response.message});
                                    }
                                },
                                error:function(data){
                                    swall_error();
                                }
                            });
                        })
                    },
                    allowOutsideClick: false,
                });
            }
        });
    });
